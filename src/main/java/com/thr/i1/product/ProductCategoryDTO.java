public class ProductCategoryDTO {
	
	private String cateName;
	private Long category;
	private Long cateRef1;
	private Long cateRef2;
	
	public String getCateName() {
		return cateName;
	}
	public void setCateName(String cateName) {
		this.cateName = cateName;
	}
	public Long getCategory() {
		return category;
	}
	public void setCategory(Long category) {
		this.category = category;
	}
	public Long getCateRef1() {
		return cateRef1;
	}
	public void setCateRef1(Long cateRef1) {
		this.cateRef1 = cateRef1;
	}
	public Long getCateRef2() {
		return cateRef2;
	}
	public void setCateRef2(Long cateRef2) {
		this.cateRef2 = cateRef2;
	}
	
	

}
